package com.yidejia.app.mall.tickets;

public class Ticket {
	private String status;
	private String ticketId;
	private String code;
	private String id;
	private String type;
	private String appointGoodsId;
	private String lowCash;
	private String name;
	private String beginDate;
	private String endDate;
	private String money;
	private String goodsId;
	private String comments;
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getTicketId() {
		return ticketId;
	}
	public void setTicketId(String ticketId) {
		this.ticketId = ticketId;
	}
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getAppointGoodsId() {
		return appointGoodsId;
	}
	public void setAppointGoodsId(String appointGoodsId) {
		this.appointGoodsId = appointGoodsId;
	}
	public String getLowCash() {
		return lowCash;
	}
	public void setLowCash(String lowCash) {
		this.lowCash = lowCash;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getBeginDate() {
		return beginDate;
	}
	public void setBeginDate(String beginDate) {
		this.beginDate = beginDate;
	}
	public String getEndDate() {
		return endDate;
	}
	public void setEndDate(String endDate) {
		this.endDate = endDate;
	}
	public String getMoney() {
		return money;
	}
	public void setMoney(String money) {
		this.money = money;
	}
	public String getGoodsId() {
		return goodsId;
	}
	public void setGoodsId(String goodsId) {
		this.goodsId = goodsId;
	}
	public String getComments() {
		return comments;
	}
	public void setComments(String comments) {
		this.comments = comments;
	}
	
}
